public class forex {
  public static void main(String[] args) {
    // 1~10
    for(int i=1;i<=10;i++){
      System.out.println(i);
    }
    System.out.println("===================");
    // 10~1
    for(int i=10;i>=1;i--){
      System.out.println(i);
    }

    // 무한루프 
    for (;;){
      System.out.println("무한루프에 걸렸습니다");
    }
  }
}
